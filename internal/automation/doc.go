// Package automation provides intelligent system automation capabilities
// for the Otedama P2P mining pool software.
//
// The automation framework includes:
//   - AutoScaler: Dynamic worker scaling based on system load
//   - SelfHealer: Automatic detection and resolution of system issues
//   - AlgorithmOptimizer: Intelligent mining algorithm selection
//   - ResourceManager: System resource optimization and management
//   - AutomationController: Central coordination of all automation features
//
// Design Principles:
//   - John Carmack: "Automation should make things better, not just different"
//   - Robert C. Martin: "Leave the code better than you found it"
//   - Rob Pike: "Don't guess, measure"
//
// The automation system operates at configurable levels (0-100) allowing
// fine-grained control over how much autonomy the system has.
package automation